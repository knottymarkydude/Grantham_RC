                                        runs at the weekends. This year I think the longest run I did was 16 miles in the month before though I had ran 26 miles 
                                        a couple of months earlier at the Baslow Boot Bash which gave invaluable time on my feet preparation as well as a 
                                        reminder about what proper hills are like to run up!
                                        Other tips for people wanting to join you next year? Buy a good head torch and give it a go, what’s the worst that can 
                                        happen!? Also, have a strategy and stick to it, be it walk up hills, went to eat etc, don’t deviate and research the route so you know what to expect and where to go & again don’t deviate from that! And if you do get lost don’t dwell on it, you’ll have all the time in the world once you’ve finished to check out where you went wrong.
